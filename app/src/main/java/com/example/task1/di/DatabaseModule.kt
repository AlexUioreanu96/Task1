package com.example.task1.di

import android.content.Context
import com.example.task1.db.MovieDBSingelton
import com.example.task1.db.MoviesDB
import com.example.task1.db.MoviesDao
import dagger.Module
import dagger.Provides
import dagger.hilt.InstallIn
import dagger.hilt.android.qualifiers.ApplicationContext
import dagger.hilt.components.SingletonComponent
import javax.inject.Singleton

@InstallIn(SingletonComponent::class)
@Module
object DatabaseModule {
    @Provides
    @Singleton
    fun provideDatabase(@ApplicationContext context: Context): MoviesDB {
        return MovieDBSingelton.getInstance(context)
    }

    @Provides
    fun provideLogDao(database: MoviesDB): MoviesDao {
        return database.getMovieDao()
    }
}
